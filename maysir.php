        <h1>Maysir</h1>
        
        <h2>A. Pengertian Maysir</h2>
        <p>Maysir adalah jenis transaksi permainan yang di dalamnya terdapat persyaratan berupa pengambilan sejumlah materi dari pihak yang kalah oleh pemenangnya. Mudahnya, istilah ini dapat dipahami sebagai judi atau taruhan. Selain diharamkan, tindakan ini juga termasuk dalam kategori dosa-dosa besar.</p>
        
        <h2>B. Dalam Kitap Suci</h2>
        <p>Disebutkan dalam Al-Quran bahwa permainan bertaruh nasib, termasuk maisir, adalah "dosa besar" dan "perbuatan keji Setan". Hal ini juga disebutkan dalam hadis.</p>
        <ul>
            <li>"Mereka menanyakan kepadamu (Muhammad) tentang khamar dan judi. Katakanlah, “Pada keduanya terdapat dosa besar dan beberapa manfaat bagi manusia. Tetapi dosanya lebih besar daripada manfaatnya." (Quran, 2:219)</li>
            <li>"Wahai orang-orang yang beriman! Sesungguhnya minuman keras, berjudi, (berkurban untuk) berhala, dan mengundi nasib dengan anak panah, adalah perbuatan keji dan termasuk perbuatan setan. Maka jauhilah (perbuatan-perbuatan) itu agar kamu beruntung. Dengan minuman keras dan judi itu, setan hanyalah bermaksud menimbulkan permusuhan dan kebencian di antara kamu, dan menghalang-halangi kamu dari mengingat Allah dan melaksanakan shalat, maka tidakkah kamu mau berhenti?." (Quran, Surah 5:90-91)</li>
            <li>"Diriwayatkan oleh Abu Hurairah: Nabi bersabda, "Barangsiapa di antara kalian yang berkata ketika bersumpah, 'Demi Al-Lātta dan Al-Uzza,' maka hendaknya mengucapkan, 'Tiada Tuhan (yang berhak disembah) selain Allah; Dan barangsiapa berkata kepada temannya, 'Kemarilah, aku akan bertaruh untukmu,' maka hendaknya ia bersedekah." (Sahih Bukhari, Kitab 78 (Laknat dan Sumpah), hadits 645)</li>
        </ul>
        
