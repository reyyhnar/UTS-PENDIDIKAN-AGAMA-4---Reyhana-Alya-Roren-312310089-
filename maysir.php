        <p>Secara terperinci berikut ini dampak negatif yang akan Anda dapatkan bila melakukan tindakan maysir, di antaranya sebagai berikut:</p>
        <ul>
            <li>Rugi secara Finansial karena Kezaliman</li>
            <li>Terciptanya Rasa Candu Berjudi</li>
            <li>Kesehatan Mental Terganggu</li>
            <li>Hubungan Sosial Terganggu</li>
            <li>Kriminalitas</li>
        </ul>
        
        <h2>D. Contoh Maysir</h2>
        <p>Maysir tak hanya terbatas pada judi atau taruhan, namun juga meliputi beberapa transaksi yang mengandung unsur-unsur sebagaimana disebutkan di atas, yakni sebagai berikut:</p>
        <ul>
            <li>Game online dengan konsep taruhan seperti Higgs Domino.</li>
            <li>SMS berhadiah dan kuis yang dilakukan melalui telepon, sebagaimana diatur dalam Fatwa MUI nomor 9 tahun 2008.</li>
            <li>Taruhan dalam bentuk togel dengan skema transaksi pembelian kupon dan menebak beberapa digit angka.</li>
            <li>Asuransi konvensional juga seringkali dianggap mengandung maysir karena terdapat spekulasi atas suatu sebab yang belum tentu terjadi di masa depan.</li>
